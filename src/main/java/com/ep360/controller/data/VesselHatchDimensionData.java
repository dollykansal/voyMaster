package com.ep360.controller.data;


public class VesselHatchDimensionData {
	private int no;
	private Integer vesselHatchDimId;
    private Double length;
    private Double beam;
	public Integer getVesselHatchDimId() {
		return vesselHatchDimId;
	}
	public void setVesselHatchDimId(Integer vesselHatchDimId) {
		this.vesselHatchDimId = vesselHatchDimId;
	}
	public Double getLength() {
		return length;
	}
	public void setLength(Double length) {
		this.length = length;
	}
	public Double getBeam() {
		return beam;
	}
	public void setBeam(Double beam) {
		this.beam = beam;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
}
